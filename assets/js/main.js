const form = document.querySelector('#hidden');
const username = document.querySelector('#username');
const button = document.querySelector('#namebtn');

function prt(event){
  event.preventDefault();
  const userName = username.value;
  if(userName=="김도현"){
    location.href="../../inner-page.html";
  }
}

var lifes = 0;

function life(){
  lifes+=1;
  const userName = username.value;
  if(lifes==3 && userName!="김도현"){
    username.classList.add('hide');
    button.classList.add('hide')
    localStorage.setItem('life','end');
    swal({
      title: "실패",
      text: "3번의 기회가 끝났습니다!\n더이상 시도할 수 없습니다.",
      icon: "error" //"info,success,warning,error" 중 택1
    })
  }else if(life !=3 & userName!="김도현"){
    swal({
      title: "틀렸습니다",
      text: `${3-lifes}번 남았습니다.\n현재 사용한 기회 ${lifes}`,
      icon: "warning" //"info,success,warning,error" 중 택1
    })
    
  }
}

function lifeChk(){
  const remaingLife = localStorage.getItem('life');
  if(remaingLife === 'end'){
    username.classList.add('hide');
    button.classList.add('hide');
  }
}



lifeChk();
button.addEventListener('click',prt);
button.addEventListener('click',life);